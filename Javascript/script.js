
function imageflip() {
    var Image_Id = document.getElementById('Barbarian');
    if (Image_Id.src.match("../Images/Minis/BarbarianFront.jpeg")) {
        Image_Id.src = "../Images/Minis/BarbarianBack.jpeg";
    }
    else {
        Image_Id.src = "../Images/Minis/BarbarianFront.jpeg";
    }
}  

